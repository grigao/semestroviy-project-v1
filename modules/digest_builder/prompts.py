"""Промпты для генерации дайджестов"""

DIGEST_SYSTEM_PROMPT = "Ты — составитель дайджестов. Проанализируй новости и верни строгий JSON по схеме."

def build_digest_prompt(items_text: str, query: str = None, today: str = "") -> str:
    query_line = f"По запросу: «{query}»\n" if query else ""
    
    return f"""Составь краткий дайджест на русском языке. Сегодня {today}.

{query_line}
Новости (цифра в начале — номер новости):
{items_text}

Верни ТОЛЬКО JSON:
{{
  "title": "Заголовок дайджеста",
  "items": [
    {{
      "summary": "Краткая аннотация (одно предложение)",
      "source_indices": [1]
    }}
  ]
}}

Правила:
- Пропусти новости, если указанная в них дата уже прошла.
- Каждая новость — отдельный пункт, не группируй.
- Аннотация — одно короткое предложение, без эмодзи.
- Возвращай ТОЛЬКО JSON."""